package main

import "fmt"

// Basic data types:

// Boleans
var isMarried bool

// int int8 int16..
var number int

// Bytes
var b byte

// Rune: Alias for int32
var c rune

// Float32 and Float64
var d float32

// Complex64 and complex128
var e complex64

func main() {

	fmt.Println(isMarried)
	fmt.Println(number)
	fmt.Println(b)
	fmt.Println(c)
	fmt.Println(d)
	fmt.Println(e)

}
